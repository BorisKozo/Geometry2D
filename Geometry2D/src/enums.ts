/**
* An enum representing the posible orientation of three points
* @class Orientation
*/
enum Orientation {
    /**
    * Clockwise
    * @property CW
    * @type Number
    * @default -1
    */
    CW = -1, //Clockwise

    /**
    * CounterClockwise
    * @property CCW
    * @type Number
    * @default 1
    */

    CCW = 1, //Counter Clockwise

    /**
    * Colinear
    * @property CL
    * @type Number
    * @default 0
    */

    CL = 0//Co-Linear
}